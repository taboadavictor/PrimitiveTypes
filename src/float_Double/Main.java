package float_Double;

public class Main {

	public static void main(String[] args) {
		
		float myMinValueFloat = Float.MIN_NORMAL;
		float myMaxValueFloat = Float.MAX_VALUE;
		//valor MIN - MAX de Float
		System.out.println(myMinValueFloat);
		System.out.println(myMaxValueFloat);
		
		double myMinValueDouble = Double.MIN_NORMAL;
		double myMaxValueDouble = Double.MAX_VALUE;
		//Valor MIN - MAX de Double
		System.out.println(myMinValueDouble);
		System.out.println(myMaxValueDouble);
		
		int myIntValue = 5;
		float myFloatValue = 5;
		double myDoubleValue = 5;
	
		System.out.println(myIntValue);
		System.out.println(myFloatValue);
		System.out.println(myDoubleValue);
		
		
	}
}